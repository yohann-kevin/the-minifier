/**
 *
 * @param {*} a
 * @param {*} b
 * @returns
 */
function mutliply(a, b) {
  return a * b; // return a multiply by b
}

/**
*
* @param {*} a
* @param {*} i
* @returns
*/
function increment(a, i) {
  if (i === a) {
    // return i multpiply a
    return i * a;
  }
  return a += i;
}

/* plop msg */
function substract(a, b) {
  return a - b;
}
